package ModuloUm;

public class Atividade01 {
    public static void main(String[] args) {
        System.out.println("Calculando o valor da Area de Um Circulo");
        /*
         * final serve para definir o valor final da variavel no codigo
         *
         */
        double raio = 5;
        final double pi = 3.14;
        double resultado = pi * (raio * raio);
        System.out.println(resultado);

    }
}
